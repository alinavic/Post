package org.example.Classes;

public class ReceptionWindows {
    int id_window, window_number;
    String status;

    public int getId_window() {
        return id_window;
    }

    public void setId_window(int id_window) {
        this.id_window = id_window;
    }

    public int getWindow_number() {
        return window_number;
    }

    public void setWindow_number(int window_number) {
        this.window_number = window_number;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
